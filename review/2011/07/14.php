<div id="detail" class="detail"><p>あの時、あの場所に　たしかに存在していた　静寂の中に潜む衝動。1st Album「TJONNIK」が一部の音楽ファンの中で話題になったTHE YOUNG GROUPの待望の2ndアルバム。1st Albumに存在していた冷たい静けさは、そのままに自分たちの内側にヒリヒリとしている熱を　最小限の空間と音像に詰め込まれた作品になっている。衝動のみで制作されたこのアルバムから聞こえてくるこのざらついた世界は彼らの表現の根底にあるもどかしさや息苦しさをも呑み込みより強度を増したギターのアンサンブルと同調しながら絡み合い、決して交わる事のないであろう喪失感と幸福感が同時に存在するような感触を与えてくれるような気がする。（Amazon.co.jpより）<p class="profile">木之下　渉 （Vo.G）土信田　有宏（G）によるユニットTHE YOUNG GROUP。木之下のどことなく儚い繊細な歌とツインギターの繰り返し奏でられる旋律がギリギリの緊張感で表現される世界。一音一音から見えてくる景色と色彩の融合は限りなく白に近い。彼らの音から聞こえてくるのは、音響的な空間の作り方や質感を通過したネオ・フォーク的な響きがある。歌のための伴奏ではなく、演奏と音が存在している上に歌が乗ってく感じが彼らが紡ぎだす出すグルーヴにつながっていく。彼らの音から聞こえてくる音像は、草原を何年も同じ様に吹きつずけるそよ風のようでもあり、長い年月をかけて、降り積もった雪景色を眺めているようでもあり、とても緩やかな時間と目の前を横たわっている空間の軸の断片をのぞき見るファインダーのような感じさえもする。この作品を聞き通したときに、ふと以前どこかで見た事のある情景に佇んでいるような感覚に戻してくれる。それは暖かくもあり、すごく冷たい場所だったりもする。（Amazon.co.jpより）</p></div>
