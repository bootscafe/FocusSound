<div id="detail" class="detail"><p>90年代から音楽制作を始め、1999年にNS-Com, 2002年にはFlyrecからアルバムをリリースし、その後は自身のElegantdiscより2枚のアルバムを残し、国内のエレクトロニカやブレイクビーツ・シーンの牽引してきた、Hidenobu ItoがWimm Recordingsより満を持して通算5作目のアルバムをリリース。80年代のソウル、R&B、ディスコさらにはハードロックまで飲み込み、甘酸っぱく暖かみのあるシンセ・メロディーで包み込み、長年積み重ねてきた卓越したカットアップ技法によるブレイクビーツで刻まれる独自解釈なファンクネス・サウンドは、ブレイクビーツに限らずバレアリックなディスコ・ハウス・ファンから、ゲットー・ビーツ・マニアにもアピール必死! そして、注目はフレンチ・ヒップホップの人気ユニット、TTCのラッパーであるCuizinierをフィーチャーしたキラー・チューン「Paris Tokyo Connection」を収録なおのです!!（Amazon.co.jpより）</p><p class="profile">エレクトロニカ/ブレイクビーツ/ハウス・アーティスト。'90年頃から音楽制作を開始。いくつかのコンピレーション、サウンドトラック等に参加。'99年にns-comよりアナログ12インチ「heaven ep」とアルバム「bedroom in the cage」をリリース。その後'02年flyrecから2ndアルバム「first love」、'05~06年自身のレーベルELEGANTDISCから2枚のアルバムを発表。近年ハウスにも取り組み始めドイツmo'sferry、fenou、Level recordsなど海外レーベルから数多くの12"をリリースし、Neneh Cherry and Sylvie MarksのMIXCD「Panik:」に"KICK"がライセンス。'07年リリースしたmo's ferryからのKrikor Remix収録のソロep"WAKE"が、ローラン・ガルニエをはじめgiles smith、Karotte (Cocoon)、Mike Monday、Mr C (The End UK)など多くのDJから賞賛を受ける。現在さまざまな国内外のレーベルからアルバムや12"のリリースを控えている。10人の女性ヴォーカルをプロデュースした『10Girls'Voice』、YOSHIMI『yyy...』、Anna Yamada『depaysement』など女性ヴォーカル物のアルバムプロデュースを多く手掛けるなど、エレクトロニカ~だけにとどまらず活動を展開している。（Amazon.co.jpより）</p></div>
