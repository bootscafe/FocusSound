<div id="detail" class="detail"><p>ソロとしてはそれぞれ00年代後半より活動を開始、2012年にユニットとなり同年12月にBunkai-Kei recordsからリリースした1stアルバム『utakata＋＋』より約2年、疋田哲也+NILによる待望の2ndフルアルバムが完成。</p><p>Electronica、IDM、Techno、Dub、Ambientなど一つのジャンルでは収まらないほどの様々な要素が鏤められた彼等従来のサウンドの良さはそのままに、心の琴線に触れる叙情的なメロディーやコード、大地と大空を駆け巡る情景的なストリングスやシンセサイザー、また聴き手の耳を離さないスムースなリズムと構成力などをはじめ、楽曲としての精度の飛躍とともにその魅力が存分に凝縮されつつ、そこに作品としての一貫性も加わり、非常に魅力的なアルバムに仕上がった。</p><p>アートワークには、第7回大野城まどかぴあ版画ビエンナーレ展池田満寿夫大賞、第4回山本鼎版画大賞展上田市長賞などを受賞した銅版画作家である上原修一の作品をフィーチャー。</p><p>マスタリングは、AOKI takamasaやAmetsubなどPROGRESSIVE FOrMの初期～中期を彩ったアルバムを手掛けた、オノセイゲンが代表をするSaidera Masteringのチーフエンジニアである森崎雅人が担当しました。</p><p class="profile">北海道出身の疋田哲也（ヒキタテツヤ）とNIL（ナイル）のプロデュース・ユニット、現在は都内を拠点としている。<br />疋田は2009年より制作を開始、NILは2006年より制作を開始しつつ2009年よりDJとしても活動。<br />2012年に疋田哲也の呼びかけで共同での音楽制作に着手。北海道での初ライブをきっかけに、東京、大阪、京都、札幌でのライブ活動を行いながら楽曲を制作。同年12月にオンライン・レーベルの旗手Bunkai-Kei recordsから1stアルバム『utakata＋＋』をリリース、また2013年初頭には渋谷の人気ライブハウスWWWのW recordsに参加、これまでにコンピレーション作品への楽曲提供やリミックスなどを多数手がける。<br />同時にBunkai-Kei records主催の《OUT OF DOTS》や関西をベースに独自の空間を演出する《IdleMoements》など国内の多数イベントに出演。疋田は2014年1月にAN/AYよりEP『Person and nature』をリリース、また同年5月にflauとベルリンのProject Mooncircleより共同リリースされたKidkanevilの『My Little Ghost』にも参加している。<br />そして2015年1月、待望の2ndアルバムをPROGRESSIVE FOrMよりリリースする。</p></div>
